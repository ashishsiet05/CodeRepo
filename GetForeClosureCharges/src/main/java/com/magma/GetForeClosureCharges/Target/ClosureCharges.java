package com.magma.GetForeClosureCharges.Target;

public class ClosureCharges {
	
	private Integer serialNo;
	private Integer orgComponentCode;
	private String componentDesc;
	private Double dueAmount;
	private String addLess;
	public Integer getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(Integer serialNo) {
		this.serialNo = serialNo;
	}
	public Integer getOrgComponentCode() {
		return orgComponentCode;
	}
	public void setOrgComponentCode(Integer orgComponentCode) {
		this.orgComponentCode = orgComponentCode;
	}
	public String getComponentDesc() {
		return componentDesc;
	}
	public void setComponentDesc(String componentDesc) {
		this.componentDesc = componentDesc;
	}
	public Double getDueAmount() {
		return dueAmount;
	}
	public void setDueAmount(Double dueAmount) {
		this.dueAmount = dueAmount;
	}
	public String getAddLess() {
		return addLess;
	}
	public void setAddLess(String addLess) {
		this.addLess = addLess;
	}
	
	

}
